
package javaprograms;


public class UnaryOperator {
    public static void main(String[] args) {
            int x = 11;
            int z;
            //Unary +
            z = +x;
            //it will the positive value;
            System.out.println(z);
            z = -x;
            //it will the negative value;
            System.out.println(z);
    
    }
}
