
package javaprograms;

public class PrintingElse {
    public static void main(String[] args) {
       boolean b = true;
       char c = 'x';
        int d = 78;
        float f = 10.34324432443245f;
      
       //using printf
        System.out.printf("Boolean of %b\n",b);
        System.out.printf("Character of %c\n",c);
        System.out.printf("Integer of %d\n",d);
        System.out.printf("Float of %.2f\n",f);
        
    }
}
